import Foundation

public enum GameBoy {
    case original
    case color
    case advance
}

extension GameBoy {
    var headerSize: Int {
        switch self {
        case .original: fallthrough
        case    .color: return 0x50
        case  .advance: return 0xC0
        }
    }
    
    var headerOffset: Int {
        switch self{
        case .original: fallthrough
        case    .color: return 0x100
        case  .advance: return 0x08000000
        }
    }
}

